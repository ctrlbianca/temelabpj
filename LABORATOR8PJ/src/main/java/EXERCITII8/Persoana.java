package EXERCITII8;

public class Persoana {
    private int id;
    private String nume;
    private int varsta;

    public Persoana(int id, String nume, int varsta) {
        this.id = id;
        this.nume = nume;
        this.varsta = varsta;
    }

    public int getId() { return id; }
    public String getNume() { return nume; }
    public int getVarsta() { return varsta; }
}

